#include <iostream>
#include <cmath>
using namespace std;

int main(){

    //declare variables
    int num;

    //Prompt the user to enter a number
    cout <<"Enter a number:"<<endl;
    cin>>num;

    //print the solutions
    cout <<"Square root: "<<sqrt(num)<<endl;
    cout <<"Tan :"<<tan(num)<<endl;
    cout <<"Cos :"<<cos(num)<<endl;
    cout <<"Round:"<<round(num)<<endl;

    return 0;
}